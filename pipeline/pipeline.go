// RepeatFn -
func RepeatFn(
	done <-chan interface{},
	fn func() interface{},

) <-chan interface{} {
	valueStream := make(chan interface{})
	go func() {
		defer close(valueStream)
		for {
			select {
			case <-done:
				return
			case valueStream <- fn():
			}
		}
	}()
	return valueStream
}

// PrimeFinder -
func PrimeFinder(
	done <-chan interface{},
	valueStream <-chan int,
) <-chan interface{} {
	primeStream := make(chan interface{})
	go func() {
		defer close(primeStream)
		for v := range valueStream {
			select {
			case <-done:
				return
			default:
				if big.NewInt(int64(v)).ProbablyPrime(0) {
					primeStream <- v
				}
			}
		}
	}()
	return primeStream
}

// Take -
func Take(
	done <-chan interface{},
	valueStream <-chan interface{},
	num int,
) <-chan interface{} {
	takeStream := make(chan interface{})
	go func() {
		defer close(takeStream)
		for i := 0; i < num; i++ {
			select {
			case <-done:
				return
			case takeStream <- <-valueStream:
			}
		}
	}()
	return takeStream
}
